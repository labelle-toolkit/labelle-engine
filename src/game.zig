        pub const camera_is_builtin = !(@hasDecl(ComponentsType, "has") and ComponentsType.has("Camera"));

        // â”€â”€ Tilemap (T2 Phase 2) â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
