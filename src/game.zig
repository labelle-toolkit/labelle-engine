/// Full game configuration â€” the assembler fills ALL comptime slots.
/// RenderImpl is a renderer plugin (e.g. gfx.GfxRenderer) satisfying RenderInterface.
/// The engine does NOT depend on labelle-gfx â€” it accesses component types through RenderImpl.
///
/// **Y-axis convention.** This entry point keeps the historical positional
/// signature (the assembler's `engine.GameConfig(...)` call ends at
/// `GameEvents,`) and defaults the project's logical Y-axis to `.up` â€” today's
/// historical behavior (labelle-engine#642). This is the **code-level**
/// default and exists purely for transition safety: now that the renderer
/// *reads* `Game.y_axis` (engine#642 threads it into `GfxRendererWith`), a
/// game generated by the *old* assembler still calls `GameConfig` with no
/// explicit axis. If that fell back to `.down` the renderer would stop
/// flipping and the game would render upside-down. Falling back to `.up`
/// keeps such games bit-identical to pre-#642.
///
/// `.down` remains the **project-config** default (the assembler's unset-guard
/// + `labelle-init`, RFC Â§3, Q1â†’(b)) â€” never the engine code default. A
/// project that wants `.down` (screen-native) goes through
/// `GameConfigWithYAxis`, which the assembler (#370) emits once it parses
/// `.y_axis` from `project.labelle`. Threading a convention this way is purely
/// additive: existing generated games keep calling `GameConfig` unchanged.
pub fn GameConfig(
    comptime RenderImpl: type,
    comptime EcsImpl: type,
    comptime InputImpl: type,
    comptime AudioImpl: type,
    comptime VideoImpl: type,
    comptime GuiImpl: type,
    comptime Hooks: type,
    comptime LogSinkImpl: type,
    comptime ComponentsType: type,
    comptime GizmoCategoriesSlice: anytype,
    comptime GameEventsParam: type,
) type {
    return GameConfigWithYAxis(
        RenderImpl,
        EcsImpl,
        InputImpl,
        AudioImpl,
        VideoImpl,
        GuiImpl,
        Hooks,
        LogSinkImpl,
        ComponentsType,
        GizmoCategoriesSlice,
        GameEventsParam,
        // Code-level default â€” `.up` preserves today's renderer flip for
        // old-assembler games that call `GameConfig` with no explicit axis
        // (engine#642). The `.down` project-config default lives in the
        // assembler/labelle-init, not here.
        .up,
    );
}

/// Y-axis-aware game configuration â€” identical to `GameConfig` but with an
/// explicit project Y-axis convention as the trailing comptime slot. The
/// assembler emits this entry point once it parses `.y_axis` from
/// `project.labelle` (labelle-engine#639 / assembler #370); until then
/// `GameConfig` delegates here with the `.down` default so the threading is
/// already in place and only the assembler's emitted call needs to change.
///
/// `y_axis_param` is surfaced as the comptime constant `Game.y_axis` (and the
/// `game.yAxis()` accessor), and is consumed by `screenToLogical` â€” the
/// axis-aware picking path â€” via core's canonical `screenToLogicalY` transform.
pub fn GameConfigWithYAxis(
    comptime RenderImpl: type,
    comptime EcsImpl: type,
    comptime InputImpl: type,
    comptime AudioImpl: type,
    comptime VideoImpl: type,
    comptime GuiImpl: type,
    comptime Hooks: type,
    comptime LogSinkImpl: type,
    comptime ComponentsType: type,
    comptime GizmoCategoriesSlice: anytype,
    comptime GameEventsParam: type,
    comptime y_axis_param: core.YAxis,
