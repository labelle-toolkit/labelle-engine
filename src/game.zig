        /// the engine's built-in emitter channel is active. Mirrors
        /// `camera_is_builtin`.
        pub const emitter_is_builtin =
            !(@hasDecl(ComponentsType, "has") and ComponentsType.has("Emitter"));

        pub const Input = @import("input.zig").InputInterface(InputImpl);

