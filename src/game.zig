        /// The renderer's layer enum (`RenderImpl.Layer`, gfx's
        /// `LayerEnum`), or `void` when the renderer doesn't expose one.
        /// The T3 tilemap Z-interleave binds `.tmx` layers to engine layers
        /// by matching a `.tmx` layer name to a `@tagName` of this enum.
        pub const RenderLayerEnum = if (@hasDecl(RenderImpl, "Layer")) RenderImpl.Layer else void;

        /// True when the tilemap Z-interleave (T3) is available. When false
        /// the engine falls back to the T2 whole-stack pre-sprite background
        /// pass (`renderTilemaps` + `renderer.render()`), so stub / older /
        /// non-standard renderers are unaffected.
        ///
        /// Requires, in order (short-circuited so a later reflection is never
        /// analyzed when an earlier guard fails â€” same defensive-comptime
        /// spirit as `tilemap_runtime.hasReflectableSeam`, gfx #711 review):
        ///   1. the decoded-map seam (`tilemap_supported`);
        ///   2. the per-layer render hook `renderWithLayerHook` (gfx â‰Ą1.22.0);
        ///   3. a `Layer` decl that is a genuine `enum` type (NOT `void` / a
        ///      non-enum stub) exposing `config()` â€” the two things the
        ///      interleave path calls on it (`stringToEnum(Layer, â€¦)` and
        ///      `layer.config().space`). Without this a renderer that declares
        ///      `renderWithLayerHook` but has `Layer = void` (or a non-config
        ///      type) would route into a path that can't compile.
        pub const tilemap_interleave_supported = interleaveSupported();

        fn interleaveSupported() bool {
            if (!tilemap_supported) return false;
            if (!@hasDecl(RenderImpl, "renderWithLayerHook")) return false;
            if (!@hasDecl(RenderImpl, "Layer")) return false;
            const L = RenderImpl.Layer;
            if (@typeInfo(L) != .@"enum") return false;
            if (!@hasDecl(L, "config")) return false;
            return true;
        }

        /// True when the tilemap BACKGROUND (unbound `.tmx` layers) can be
        /// drawn PER active camera: the renderer exposes the dual-hook
        /// `renderWithLayerHooks` (gfx â‰Ą1.24.0), whose `on_before_layers`
        /// callback runs once per active camera inside that camera's transform
        /// + viewport scissor, before the sprite stack. Builds on
        /// `tilemap_interleave_supported`. When false but interleave IS
        /// supported (a renderer with only the older single-callback
        /// `renderWithLayerHook`, gfx 1.22â€“1.23) the background falls back to
        /// the single primary-camera pass; BOUND layers are per-camera either
        /// way. `@hasDecl` on the valid `RenderImpl` is always safe, so a
        /// plain `and` suffices here.
        pub const tilemap_percamera_background_supported = tilemap_interleave_supported and
            @hasDecl(RenderImpl, "renderWithLayerHooks");

        // â”€â”€ Particles / Emitter (#750) â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
        /// Engine built-in `Emitter` component (authors a particle emitter;
        /// see `src/emitter.zig`). Handled by dedicated built-in channels
        /// (scene loader, side-table runtime) â€” NOT a `ComponentRegistry`
        /// component, unless a project registers its own `Emitter`, which
        /// takes precedence (the scene-loader branch is gated on
        /// `!Components.has("Emitter")`).
        pub const EmitterComp = emitter_mod.Emitter;
